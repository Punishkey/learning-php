El bucle for hace exactamente lo mismo que el while, pero es mucho más compacto. Se utiliza cuando sabes de antemano cuántas veces exactas quieres que se repita una tarea.

En lugar de poner el contador fuera y el incremento dentro, el for mete las tres herramientas en la misma línea separadas por puntos y comas ;:
PHP

<?php

// for (inicio; condición; incremento)
for ($i = 1; $i <= 5; $i++) {
    echo "Vuelta número: " . $i . "<br>";
}

    💡 Nota de jerga: En programación, es una tradición usar la variable $i (de índice o iterador) dentro de los bucles for.

