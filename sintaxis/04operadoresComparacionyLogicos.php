Para comparar variables, PHP utiliza estos operadores:
Operador	Significado	Ejemplo	Resultado en tu caso
==	Igual a	$experiencia == 20	true (Verdadero)
===	Idéntico a (Mismo valor y mismo tipo)	$experiencia === "20"	false (Porque uno es número y el otro texto)
!=	Diferente de	$experiencia != 10	true
>	Mayor que	$experiencia > 5	true
<	Menor que	$experiencia < 30	true
>=	Mayor o igual que	$experiencia >= 20	true
<=	Menor o igual que	$experiencia <= 15	false

    ⚠️ ¡Cuidado clásico! No es lo mismo un solo signo de igual (=) que dos (==).

        Un solo = sirve para guardar un valor en una variable (asignación).

        El doble == sirve para comparar si dos cosas son iguales.

